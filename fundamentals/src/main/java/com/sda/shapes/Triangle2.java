public class Triangle2 {
    public static void print(int nr) {
        for (int i = 1; i <= nr; i++) {
            System.out.print("# ");
        }
    }
    public static void main(String[] args) {
        print(8);
        System.out.println();
        print(7);
        System.out.println();
        print(6);
        System.out.println();
        print(5);
        System.out.println();
        print(4);
        System.out.println();
        print(3);
        System.out.println();
        print(2);
        System.out.println();
        print(1);
    }
}
