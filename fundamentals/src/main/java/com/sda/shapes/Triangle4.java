public class Triangle4 {
    public static void print(int blanck){
        for(int j=1;j<=blanck;j++){
            System.out.print("  ");
        }
        for(int i=blanck;i<8;i++){
            System.out.print("# ");
        }
    }

    public static void main(String[] args) {
        print(7);
        System.out.println();
        print(6);
        System.out.println();
        print(5);
        System.out.println();
        print(4);
        System.out.println();
        print(3);
        System.out.println();
        print(2);
        System.out.println();
        print(1);
        System.out.println();
        print(0);
        System.out.println();
    }
}
